// This program demonstrates the use of data types in Dart

// int: a data type for whole numbers
int num1 = 10; // declare and initialize an int variable
print(num1); // print the value of num1

// double: a data type for floating-point numbers
double num2 = 3.14; // declare and initialize a double variable
print(num2); // print the value of num2

// String: a data type for sequences of characters
String name = "Alice"; // declare and initialize a String variable
print(name); // print the value of name

// List: a data type for ordered collections of objects
List<int> numbers = [1, 2, 3, 4, 5]; // declare and initialize a List of int
print(numbers); // print the value of numbers
print(numbers[0]); // print the first element of numbers

// Map: a data type for collections of key-value pairs
Map<String, int> scores = {"Alice": 90, "Bob": 80, "Charlie": 70}; // declare and initialize a Map of String and int
print(scores); // print the value of scores
print(scores["Alice"]); // print the value associated with the key "Alice"
